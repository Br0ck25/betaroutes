Go Route Yourself – Privacy Policy

Last Updated: 12/8/2025

Go Route Yourself (“we,” “our,” or “us”) provides route-planning and trip-tracking tools designed to help users manage routes, track travel data, store logs, and sync information across devices. We value your privacy and are committed to protecting your personal information.

This Privacy Policy explains what information we collect, how we use it, and your rights regarding your data.

1. Information We Collect
1.1 Information You Provide

We may collect the following information when you use Go Route Yourself:

Account Information: Email address, password, username, and authentication details.

Saved Data: Saved routes, trip logs, imported GPX/GeoJSON files, waypoints, folders, and track names.

Support Requests: Any information you voluntarily provide when contacting support.

1.2 Automatically Collected Information

When you use the service, we may automatically collect:

Device Information: Browser type, device type, operating system, and IP address.

Usage Data: Features you access, pages viewed, buttons clicked, performance logs, and error diagnostics.

Location Information (when enabled):

Real-time location for navigation or trip recording.

GPS tracks when recording trips.

We only collect location when you grant permission.

1.3 Storage and Services

Depending on your account type and features used, data may be stored using:

Cloudflare Workers/KV Storage

Cloudflare R2 (for uploaded GPX files or track data)

LocalStorage on your device (for offline use and faster loading)

We do not sell or share your uploaded tracks or personal data with third parties.

2. How We Use Your Information

We use your information to:

Provide, maintain, and improve the Go Route Yourself service

Sync routes, tracks, and settings across devices

Authenticate users and prevent unauthorized access

Display your GPS location and track your trips (only when enabled)

Process imports and exports of GPX/GeoJSON

Offer customer support and troubleshoot issues

Analyze usage to improve performance and new features

Send optional service updates or notifications

We do not use your personal information for advertising or profiling.

3. How Your Data Is Stored

Depending on the feature:

Your private tracks and logs are stored in an account-protected area of Cloudflare KV or R2.

Public tracks are visible to all users if you choose to publish them.

LocalStorage may store temporary app preferences or offline data.

GPS data is only stored when you manually save a track.

Real-time location data is not stored unless you are actively recording a trip.

We use industry-standard security measures and Cloudflare infrastructure to protect your data.

4. Sharing and Disclosure

We do not sell, rent, or trade your data.

We may share data only in these cases:

Legal Requirements: If required by law, court order, or governmental request.

Security Protections: To investigate potential violations, fraud, or threats.

Service Providers: Limited technical partners that support hosting (e.g., Cloudflare), who are contractually obligated to keep data secure.

We do not share user location or GPS data with third parties.

5. Your Rights and Choices

Depending on your jurisdiction, you have rights including:

Access the data in your account

Update or edit your information

Delete your account and associated data

Export your tracks or logs (CSV/JSON)

Opt out of non-essential emails

You can request deletion or export by contacting support at:
support@gorouteyourself.com

6. Children’s Privacy

Go Route Yourself is not intended for children under 13.
We do not knowingly collect information from minors.
If we learn that a child’s data has been collected, we will delete it promptly.

7. Cookies & Tracking Technologies

We may use:

Essential cookies for login sessions

Performance analytics (privacy-focused and non-identifying)

Local storage for offline maps and saved preferences

We do not use advertising trackers or third-party behavioral tracking.

8. Data Retention

We retain data only as long as necessary to:

Provide the service

Maintain user accounts

Meet legal obligations

You may delete your data at any time through your account or by requesting removal.

9. International Users

If you access Go Route Yourself outside the United States, your information may be processed in the U.S. or other regions where our systems operate.
We use privacy-compliant vendors to ensure data protection.

10. Changes to This Privacy Policy

We may update this policy from time to time.
We will notify users of significant changes through:

A notice in the app or website, or

Email notification (if applicable)

Continued use after changes means acceptance of the updated policy.

